// Returns the Unicode block constant for a character, or None if not found
///
/// This function performs a binary search on the Unicode block ranges
/// to determine which block a character belongs to.
///
/// # Arguments
/// * `ch` - The character to classify.
///
/// # Returns
/// The Unicode block constant (e.g., UNICODE_BASIC_LATIN) or None if the
/// character doesn't belong to any defined block.
pub fn unicode_block(ch: char) -> Option<u16> {
    let cp = ch as u32;
    if cp <= 0x7F {
        return Some(UNICODE_BASIC_LATIN);
    }
    // Binary search for the correct block
    let mut be = 0;
    let mut en = UNICODE_BLOCKS.len() as isize - 1;
    while be <= en {
        let mid = (be + en) >> 1;
        let (name, start, end) = UNICODE_BLOCKS[mid as usize];
        if start <= cp && cp <= end {
            return Some(name);
        }
        if cp < start {
            en = mid - 1;
        } else {
            be = mid + 1;
        }
    }
    None
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_unicode_block() {
        assert_eq!(unicode_block('\u{0065}').unwrap(), UNICODE_BASIC_LATIN);
        assert_eq!(unicode_block('\u{007F}').unwrap(), UNICODE_BASIC_LATIN);
        assert_eq!(unicode_block('\u{0080}').unwrap(), UNICODE_LATIN_1_SUPPLEMENT);
        assert_eq!(unicode_block('\u{21FF}').unwrap(), UNICODE_ARROWS);
        assert_eq!(unicode_block('\u{2200}').unwrap(), UNICODE_MATHEMATICAL_OPERATORS);
        assert_eq!(unicode_block('\u{2201}').unwrap(), UNICODE_MATHEMATICAL_OPERATORS);
        assert_eq!(unicode_block('\u{22FF}').unwrap(), UNICODE_MATHEMATICAL_OPERATORS);
        assert_eq!(unicode_block('\u{2300}').unwrap(), UNICODE_MISCELLANEOUS_TECHNICAL);
        // Wide Unicode tests
        assert_eq!(unicode_block('\u{F0000}').unwrap(), UNICODE_SUPPLEMENTARY_PRIVATE_USE_AREA_A);
        assert_eq!(unicode_block('\u{FFFFF}').unwrap(), UNICODE_SUPPLEMENTARY_PRIVATE_USE_AREA_A);
        assert_eq!(unicode_block('\u{100000}').unwrap(), UNICODE_SUPPLEMENTARY_PRIVATE_USE_AREA_B);
        assert_eq!(unicode_block('\u{10FFFF}').unwrap(), UNICODE_SUPPLEMENTARY_PRIVATE_USE_AREA_B);
    }
}
